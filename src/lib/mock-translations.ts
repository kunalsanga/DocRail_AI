export function getMockTranslation(text: string, sourceLang: 'en' | 'ml', targetLang: 'en' | 'ml'): string {
  if (sourceLang === targetLang) {
    return text;
  }
  
  const translationKey = `${sourceLang}_to_${targetLang}`;
  const translations = mockTranslations[translationKey as keyof typeof mockTranslations];
  
  if (translations && translations[text as keyof typeof translations]) {
    return translations[text as keyof typeof translations];
  }
  
  // Fallback for untranslated text
  return `${text} [${targetLang === 'ml' ? 'മലയാളം' : 'English'} - Mock Translation]`;
}
